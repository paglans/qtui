# ── AveragedScalar ───────────────────────────────────────────────────
# Reads an AI PV at 50 ms intervals over exposure_time seconds and
# returns the mean.  Fully compatible with bluesky plans — the RE
# waits for trigger() to complete before moving to the next point,
# so the motor is always stationary during acquisition.

class AveragedScalar(Device):
    """Time-averaging scalar detector for analog signals.

    Samples the PV every 50 ms for `exposure_time` seconds and reports
    the mean, standard deviation, and sample count.  The `exposure_time`
    attribute is set at runtime by the custom plan wrappers in 15_plans.py.
    """
    raw = Cpt(EpicsSignalRO, "")

    def __init__(self, *args, exposure_time: float = 1.0, **kwargs):
        super().__init__(*args, **kwargs)
        self.exposure_time = exposure_time
        self._mean  = float("nan")
        self._std   = float("nan")
        self._count = 0

    def trigger(self):
        status = DeviceStatus(self)

        def _acquire():
            samples = []
            t0 = _time.monotonic()
            while _time.monotonic() - t0 < self.exposure_time:
                v = self.raw.get()
                if v is not None:
                    samples.append(float(v))
                _time.sleep(0.05)
            if samples:
                self._mean  = float(_np.mean(samples))
                self._std   = float(_np.std(samples))
                self._count = len(samples)
            else:
                self._mean = self._std = float("nan")
                self._count = 0
            status.set_finished()

        threading.Thread(target=_acquire, daemon=True).start()
        return status

    def read(self):
        ts = _time.time()
        return {
            self.name:           {"value": self._mean,  "timestamp": ts},
            self.name + "_std":  {"value": self._std,   "timestamp": ts},
            self.name + "_n":    {"value": self._count, "timestamp": ts},
        }

    def describe(self):
        src = self.raw.pvname
        return {
            self.name:           {"source": src, "dtype": "number", "shape": []},
            self.name + "_std":  {"source": src, "dtype": "number", "shape": []},
            self.name + "_n":    {"source": src, "dtype": "number", "shape": []},
        }

