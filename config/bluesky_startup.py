# ── Per-scan HDF5 export (h5py + event-model, no suitcase needed) ────
try:
    import h5py as _h5py
    import numpy as _np
    from event_model import RunRouter

    class _HDF5Callback:
        """Accumulates bluesky documents for one run and writes an HDF5 file
        on stop.  One instance per run, created by _hdf5_factory.

        Compatible with event-model >= 1.14.0: RunRouter calls the callback
        directly as callback(name, doc), so __call__ dispatches to the
        appropriate method.
        """

        def __init__(self, path: str):
            self._path      = path
            self._start_doc = {}
            self._columns   = []
            self._data      = {}

        def __call__(self, name: str, doc: dict):
            """Dispatcher — event-model >= 1.14.0 calls callbacks this way."""
            getattr(self, name, lambda d: None)(doc)

        def start(self, doc):
            self._start_doc = doc

        def descriptor(self, doc):
            if self._columns:
                return  # use first descriptor only
            hints   = doc.get("hints", {})
            dims    = [d[0][0] for d in hints.get("dimensions", []) if d[0]]
            others  = [k for k in doc["data_keys"] if k not in dims]
            self._columns = dims + others
            self._data    = {k: [] for k in self._columns}

        def event(self, doc):
            data = doc.get("data", {})
            for k in self._columns:
                self._data[k].append(data.get(k, float("nan")))

        def event_page(self, doc):
            data = doc.get("data", {})
            n = len(next(iter(data.values()), []))
            for i in range(n):
                for k in self._columns:
                    col = data.get(k, [float("nan")] * n)
                    self._data[k].append(
                        col[i] if i < len(col) else float("nan"))

        def stop(self, doc):
            if not any(self._data.values()):
                print(f"[startup] HDF5: no data for {self._path}")
                return
            try:
                with _h5py.File(self._path, "w") as f:
                    meta = f.create_group("scan/metadata")
                    for k, v in self._start_doc.items():
                        try:
                            meta.attrs[k] = str(v)
                        except Exception:
                            pass
                    meta.attrs["file_format"] = "AMBER_HiRRIXS_HDF5_v1"
                    meta.attrs["beamline"]    = "ALS BL601 AMBER"
                    dgrp = f.create_group("scan/data")
                    for k, vals in self._data.items():
                        if vals:
                            dgrp.create_dataset(
                                k, data=_np.array(vals, dtype=float))
                print(f"[startup] HDF5 written → {self._path}")
            except Exception as exc:
                print(f"[startup] HDF5 error: {exc}")

    def _hdf5_factory(name, doc):
        if name != "start":
            return [], []
        uid8 = doc.get("uid", "unknown")[:8]
        num  = doc.get("scan_id", 0)
        dts  = _dt.fromtimestamp(doc.get("time", 0)).strftime("%Y%m%dT%H%M%S")
        # The GUI-supplied file path arrives at the top level of the start doc
        # (bluesky-queueserver flattens item meta into the start document).
        # Fall back to auto-generated ~/Data path for scans run outside the GUI.
        gui_path = doc.get("file", "")
        if gui_path:
            path = gui_path
            Path(path).parent.mkdir(parents=True, exist_ok=True)
        else:
            path = str(_EXPORT_DIR / f"scan_{num:04d}_{uid8}_{dts}.h5")
        print(f"[startup] HDF5 → {path}")
        return [_HDF5Callback(path)], []

    RE.subscribe(RunRouter([_hdf5_factory]))
    print(f"[startup] HDF5 export active → {_EXPORT_DIR}")

except ImportError as _imp_e:
    print(f"[startup] HDF5 export unavailable: {_imp_e}")
    print("[startup] Install with: pip install event-model h5py")

